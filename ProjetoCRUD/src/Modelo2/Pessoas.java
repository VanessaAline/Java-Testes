package Modelo2;

//Primeiro a ser Criado\\

public class Pessoas {
	
	//Atributos\\
	private int IdPessoas;
	private String nome;
	private String sexo;
	private int idade;
	
	//Construtor\\
	
	//Get´s & Set´s\\
	public int getIdPessoas() {
		return IdPessoas;
	}

	public void setIdPessoas(int idPessoas) {
		IdPessoas = idPessoas;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getSexo() {
		return sexo;
	}

	public void setSexo(String sexo) {
		this.sexo = sexo;
	}

	public int getIdade() {
		return idade;
	}

	public void setIdade(int idade) {
		this.idade = idade;
	}
	
	
	
	
}
